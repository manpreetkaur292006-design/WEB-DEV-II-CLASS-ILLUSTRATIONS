
// JS OBJECTS

// They are collections of key-value pairs where each key is a string
// (or a symbol) and each value can be any data type, including other objects.

// EXAMPLE :

let student = {
    Sname : "Manpreet Kaur",
    Sroll : 2501010070,
    age : 20,
    dept : "SOET"
}

console.log(student)

console.log(student.Sname)
console.log(student["age"])

console.log(student.Sroll)
console.log(student["dept"])

student.Sname = "Manpreet"
console.log(student.Sname)

console.log(student)

