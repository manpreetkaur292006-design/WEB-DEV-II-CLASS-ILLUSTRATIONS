// DIFFERENT TYPES OF FUNCTION IN JS :-

// 1. REGULAR FUNCTION : Defined using "function" keyword.
function greet(name){
    return "Hello, $(name)!";
}
console.log(greet("Manpreet"))

// 2. ARROW FUNCTION 
const greet=(name)=>"Hello, $(name)!";
console.log(greet("Manpreet"))

